package mvcexample;

public class StudentVO {
	private String rollNo; // 학번
	private String name;   // 이름
	
	// getter , setter
	public String getRollNo() {
		return rollNo;
	}
	public void setRollNo(String rollNo) {
		this.rollNo = rollNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
